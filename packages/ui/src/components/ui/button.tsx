import { cn } from "@/lib/utils";
import { Slot } from "@radix-ui/react-slot";
import { type VariantProps, cva } from "class-variance-authority";
import { type ButtonHTMLAttributes, forwardRef } from "react";

const buttonVariants = cva(
  "inline-flex items-center justify-center gap-2 whitespace-nowrap rounded-md text-sm font-medium transition-colors focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50 [&_svg]:pointer-events-none [&_svg]:size-4 [&_svg]:shrink-0",
  {
    variants: {
      variant: {
        default:
          "bg-crypto-blue text-white hover:bg-crypto-blue/90 focus-visible:ring-crypto-blue/50",
        destructive: "bg-red-500 text-white hover:bg-red-500/90 focus-visible:ring-red-500/50",
        outline:
          "border border-border bg-background hover:bg-accent hover:text-accent-foreground focus-visible:ring-crypto-blue/50",
        secondary:
          "bg-crypto-purple text-white hover:bg-crypto-purple/90 focus-visible:ring-crypto-purple/50",
        ghost: "hover:bg-accent hover:text-accent-foreground focus-visible:ring-crypto-blue/50",
        link: "text-crypto-blue underline-offset-4 hover:underline focus-visible:ring-crypto-blue/50",
        success:
          "bg-crypto-green text-white hover:bg-crypto-green/90 focus-visible:ring-crypto-green/50",
        warning:
          "bg-crypto-orange text-white hover:bg-crypto-orange/90 focus-visible:ring-crypto-orange/50",
      },
      size: {
        default: "h-10 px-4 py-2",
        sm: "h-9 rounded-md px-3 text-xs",
        lg: "h-11 rounded-md px-8 text-base",
        icon: "h-10 w-10",
        "icon-sm": "h-8 w-8",
        "icon-lg": "h-12 w-12",
      },
    },
    defaultVariants: {
      variant: "default",
      size: "default",
    },
  },
);

export interface ButtonProps
  extends ButtonHTMLAttributes<HTMLButtonElement>,
    VariantProps<typeof buttonVariants> {
  asChild?: boolean;
  loading?: boolean;
  error?: string;
}

const Button = forwardRef<HTMLButtonElement, ButtonProps>(
  (
    {
      className,
      variant,
      size,
      asChild = false,
      loading = false,
      children,
      disabled,
      error,
      ...props
    },
    ref,
  ) => {
    const Comp = asChild ? Slot : "button";

    return (
      <Comp
        className={cn(buttonVariants({ variant, size, className }), error && "border-destructive")}
        ref={ref}
        disabled={disabled || loading}
        aria-describedby={error ? `${props.id}-error` : undefined}
        {...props}
      >
        {loading && (
          <svg
            className="-ml-1 mr-2 h-4 w-4 animate-spin"
            xmlns="http://www.w3.org/2000/svg"
            fill="none"
            viewBox="0 0 24 24"
            aria-hidden="true"
          >
            <circle
              className="opacity-25"
              cx="12"
              cy="12"
              r="10"
              stroke="currentColor"
              strokeWidth="4"
            />
            <path
              className="opacity-75"
              fill="currentColor"
              d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"
            />
          </svg>
        )}
        {children}
      </Comp>
    );
  },
);
Button.displayName = "Button";

export { Button, buttonVariants };
